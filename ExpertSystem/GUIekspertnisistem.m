set_param('ANNmreza','SimulationCommand','start');
izlaz1=evalin('base','simout');

if izlaz1(1,2)<=30 && izlaz1(1,2)>=0
    set(handles.edit15,'string','Niska');
elseif izlaz1(1,2)<=60 && izlaz1(1,2)>=20
    set(handles.edit15,'string','Srednja');
else
    set(handles.edit15,'string','Visoka');
end
set(handles.edit14,'string',izlaz1(1,2));

load_system('simulinkekspertni');
set_param('simulinkekspertni','SimulationCommand','start');
izlaz2=evalin('base','izlazSim');

set(handles.edit16,'string',izlaz2(1,2));


if izlaz2(1,2)<=30 && izlaz2(1,2)>=0
    set(handles.edit17,'string','Niska');
elseif izlaz2(1,2)<=60 && izlaz2(1,2)>=20
    set(handles.edit17,'string','Srednja');
else
    set(handles.edit17,'string','Visoka');
